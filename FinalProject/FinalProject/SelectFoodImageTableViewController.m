#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.foodImageArray.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    SelectFoodImageTableViewCell * cell = [tableView dequeueReusableCellWithIdentifier:@"SelectFoodImageTableViewCell"];

    if(!cell)
    {
        cell = [[SelectFoodImageTableViewCell alloc]initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"SelectFoodImageTableViewCell"];
    }

    cell.foodImageView.image = [UIImage imageNamed:self.foodImageArray[indexPath.row]];
    cell.foodNameLabel.text = self.foodImageArray[indexPath.row];

    return cell;
}

- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath
{
    return 60;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    [self.selectFoodImageDelegate imageDidSelect:self.foodImageArray[indexPath.row]];
    [self.navigationController popViewControllerAnimated:YES];
}

